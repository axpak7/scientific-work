function y_final = OL4(f, x0, xend, y0, N, n0, n1, n2)
%f - ������ ���������� �� ������� ������ ������
% f(X,Y,i) � i-� ��������� f
%x0 - ��������� �������� �������
%xend - �������� �������� ������
%y0 - ������ ��������� ����������
%N - ����� �����
%[n0 n1 n2] - ���������� ��������� � �����, ������ � ������ �����������
%������� ��������������

%constants
h = (xend-x0)/N;
neq = n0 + n1 + n2;

%1 - ������ ������ � ������� ������
% n0 � ��������� ������ �� ������� ������
n11 = n0 + 1; % ������ ������ � ������ ������
n12 = n0 + n1; % ��������� ������ � ������ ������
n21 = n12 + 1; % ������ ������ �� ������ ������
n22 = n12 + n2; % n22 = neq � ����� ����� ���������, ��������� ������ �� ������ ������
y1 = zeros(neq, 1);


% ���� ���, ��������, �� ��� �����:
% ��������� K � X, Y
for j = 1:N
  % ������ ����
    K1 = zeros(neq,1);
    % ������ 0
    X = x0; % ��������� �����
    Y = y0; % ��������� �����
    for i = 1:n0
        K1(i) = h * f(X,Y,i);
    end

    % ������ 1
    X = x0; % ��������� �����
    Y = y0; % ��������� �����
    for i = n11:n12
        K1(i) = h * f(X,Y,i);
    end

    % ������ 2
    X = x0 + 1/6 * h;
    Y = y0 + 1/6 * K1;
    for i = n21:n22
        K1(i) = h * f(X,Y,i);
        Y(i) = y0(i) + 1/6 * K1(i);
    end

    % ������ ����
    K2 = zeros(neq,1);
    % ������ 0
    X = x0 + 1/3 * h;
    Y = y0 + 1/3 * K1;
    for i = 1:n0
        K2(i) = h * f(X,Y,i);
    end

    % ������ 1
    X = x0 + 1/3 * h;
    Y(1:n0) = y0(1:n0) + 1/6 * K1(1:n0) + 1/6 * K2(1:n0); % �� ������ 0
    Y(n21:n22) = y0(n21:n22) + 1/3 * K1(n21:n22); % �� ������ 2

    for i = n11:n12
        K2(i) = h * f(X,Y,i);
        Y(i) = y0(i) + 1/6 * K1(i) + 1/6 * K2(i);
    end
    
    % ������ 2
    X = x0 + 2/3 * h;
    Y(1:n0) = y0(1:n0) - 1/12 * K1(1:n0) + 3/4 * K2(1:n0);
    Y(n11:n12) = y0(n11:n12) - 1/12 * K1(n11:n12) + 3/4 * K2(n11:n12);
    
    for i=n21:n22
        K2(i) = h * f(X,Y,i);
        Y(i) = y0(i) + 1/2 * K1(i) + 1/6 * K2(i);
    end
    
    % ������ ����
    K3 = zeros(neq,1);
    %������ 0
    X = x0 + 2/3 * h;
    Y(1:n0) = y0(1:n0) - 1/3 * K1(1:n0) + K2(1:n0);
    Y(n11:n12) = y0(n11:n12) - 1/3 * K1(n11:n12) + K2(n11:n12);
    Y(n21:n22) = y0(n21:n22) + 4/9 * K1(n21:n22) + 2/9 * K2(n21:n22);
    for i = 1:n0
        K3(i) = h * f(X,Y,i);
    end
    
    % ������ 1
    X = x0 + 5/6 * h;
    Y(1:n0) = y0(1:n0) + 5/48 * K1(1:n0) + 5/12 * K2(1:n0) + 5/16 * K3(1:n0); % �� ������ 0
    Y(n21:n22) = y0(n21:n22) + 5/12 * K1(n21:n22) + 5/12 * K2(n21:n22); % �� ������ 2

    for i = n11:n12
        K3(i) = h * f(X,Y,i);
        Y(i) = y0(i) + 1/24 * K1(i) + 5/8 * K2(i) + 1/6 * K3(i);
    end
    
    % ������ 2
    X = x0 + h;
    Y(1:n0) = y0(1:n0) + K1(1:n0) - 5/4 * K2(1:n0) + 5/4 * K3(1:n0);
    Y(n11:n12) = y0(n11:n12) + 3/4 * K1(n11:n12) - 5/12 * K2(n11:n12) + 2/3 * K3(n11:n12);
    
    for i=n21:n22
        K3(i) = h * f(X,Y,i);
        Y(i) = y0(i) + 1/6 * K1(i) + 5/6 * K2(i);
    end
    
    % �������� ����
    K4 = zeros(neq,1);
    %������ 0
    X = x0 + h;
    Y(1:n0) = y0(1:n0) + K1(1:n0) - K2(1:n0) + K3(1:n0);
    Y(n11:n12) = y0(n11:n12) + 4/5 * K1(n11:n12) - 1/3 * K2(n11:n12) + 8/15 * K3(n11:n12);
    Y(n21:n22) = y0(n21:n22) + 1/3 * K1(n21:n22) + 2/3 * K2(n21:n22);
    for i = 1:n0
        K4(i) = h * f(X,Y,i);
    end
    
    % ����:
    y1(1:n0) = y0(1:n0) + 1/8 * (K1(1:n0) + 3 * K2(1:n0) + 3 * K3(1:n0) + K4(1:n0));
    y1(n11:n12) = y0(n11:n12) + 1/10 * (K1(n11:n12) + 5 * K2(n11:n12) + 4 * K3(n11:n12));
    y1(n21:n22) = y0(n21:n22) + 1/10 * (4 * K1(n21:n22) + 5 * K2(n21:n22) + K3(n21:n22));
    
    x0 = x0 + h;
    y0 = y1; % ��� ���������� ����
end

y_final = y1;

end

